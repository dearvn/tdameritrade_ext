from setuptools import find_packages, setup, Command

# Package meta-data.
NAME = 'tdameritrade_ext'
DESCRIPTION = 'This package extend from https://github.com/timkpaine/tdameritrade.'
URL = 'https://github.com/dearvn/tdameritrade_ext'
EMAIL = 'donald.nguyen.it@gmail.com'
AUTHOR = 'Donald'
REQUIRES_PYTHON = '>=3.7.0'
VERSION = '0.0.1'

# What packages are required for this module to be executed?
REQUIRED = [
    "ipython>=7.0.1",
    "pandas>=0.22.0",
    "pillow>=5.3.0",
    "requests>=2.23.0",
    "selenium>=3.141.0",
    "ujson>=1.35",
]

# What packages are optional?
EXTRAS = [
    "black>=20.",
    "bump2version>=1.0.0",
    "flake8>=3.7.8",
    "flake8-black>=0.2.1",
    "mock",
    "pytest>=4.3.0",
    "pytest-cov>=2.6.1",
    "Sphinx>=1.8.4",
    "sphinx-markdown-builder>=0.5.2",
] + REQUIRED

# The rest you shouldn't have to touch too much :)
# ------------------------------------------------
